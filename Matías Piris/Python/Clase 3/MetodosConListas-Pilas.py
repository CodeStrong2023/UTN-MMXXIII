# Agregar elementos a la pila por el final
# El último elemento en entrar es el primero en salir

Pila = [1, 2, 3, 4, 5]

Pila.append(6)
Pila.append(7)
print("Pila después de agregar elementos:", Pila)

# Sacar elementos de la pila por el final

Borrado = Pila.pop()
print('Sacamos el último elemento:', Borrado)
print('La pila se queda de la siguiente manera:')
print(Pila)




