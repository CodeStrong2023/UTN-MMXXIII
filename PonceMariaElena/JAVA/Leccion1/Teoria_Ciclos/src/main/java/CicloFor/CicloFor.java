package CicloFor;

public class CicloFor {
    public void cicloFor() {
        /* El ciclo for ejecuta el ciclo cuando la condicion sea verdadera e introduce un incremento o decremento hasta que no se cumpla */
        /*Se conoce la cantidad de iteraciones de antemano */
        
        for ( var contador = 0; contador < 7; contador++) {
            System.out.println("contador = " + contador);
        }
    }
}