# Repaso diccionarios
diccionarioNuevo = {'Azul': 'Blue', 'Rojo': 'Red',
                    'Verde': 'Green', 'Amarillo': 'Yellow'}
print(diccionarioNuevo)

# como eliminar un elemento del diccionario
del (diccionarioNuevo['Azul'])
print(diccionarioNuevo)


# los diccionarios pueden almacenar diferente tipo de datos
diccionario2 = {'Ariel': {'edad': 25, 'estatura': 1.75},
                'Osvaldo': [30, 1.80], 'Natalia': [35, 1.67]}
print(diccionario2)
