# Crear un diccionario

NuevoDiccionario = {
    'Azul': 'Blue',
    'Rojo': 'Red',
    'Verde': 'Green',
    'Amarillo': 'Yellow'
}

# Imprimir el diccionario

print(NuevoDiccionario)

# Eliminar un elemento del diccionario

del NuevoDiccionario['Azul']
print(NuevoDiccionario)

# Los diccionarios pueden almacenar diferentes tipos de datos

Dic2 = {
    'Ariel': {'edad': 25, 'estatura': 1.75},
    'Osvaldo': [30, 1.80],
    'Natalia': [35, 1.67]
}

# Imprimir el segundo diccionario

print(Dic2)
